
public class tarjeta {
    String nombre;
    double puntos;
    double credito;
    double mensualidad;
    double interes;
    
    

    @Override
    public String toString() {
        return "tarjeta{" + "nombre=" + nombre + ", puntos=" + puntos + ", credito=" + credito + ", mensualidad=" + mensualidad + ", interes=" + interes + '}';
    }
    
    
}
